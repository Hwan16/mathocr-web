-- 0019: 신고 이미지 파기 시각 기록
-- ============================================
-- 개인정보처리방침 제3조 약속("오변환 신고 이미지: 신고 검수 및 보상 처리가
-- 완료되면 지체 없이 파기")의 이행 증빙.
--
-- 관리자가 신고를 종결 상태(reviewed / rejected / accepted)로 바꾸는 순간
-- 서버가 Storage 'reports' 버킷의 이미지 객체를 삭제하고 이 컬럼에 파기
-- 시각을 남긴다. null = 아직 파기되지 않음(접수 상태이거나 이미지가 없던 신고).
--
-- 이 마이그레이션이 미적용인 환경에서도 파기 자체는 동작한다(서버가 컬럼
-- 부재 시 경로만 비우는 폴백) — 컬럼은 증빙 기록용.

alter table public.conversion_reports
  add column if not exists images_deleted_at timestamptz;
